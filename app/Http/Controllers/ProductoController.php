    public function buscar(Request $request)
    {
        $nombre = $request->input('nombre');
        $categorias_padre = Producto::where('nombre', 'LIKE', '%' . $nombre . '%')->paginate();
        return view('admin.producto.tabla', [
            'listado' => $categorias_padre
        ]);
    }


